class TestEdgeCases:
    """Tests for edge cases and error handling."""
    
    @pytest.fixture
    def converter(self):
        return MarkdownToLatexConverter()
    
    def test_empty_markdown(self, converter):
        """Test conversion of empty markdown."""
        result, refs, figs = converter.convert("")
        assert result == ""
        assert refs == []
        assert figs == []
    
    def test_only_whitespace(self, converter):
        """Test conversion of whitespace-only markdown."""
        result, _, _ = converter.convert("   \n\n   ")
        # Should produce empty or whitespace output
        assert result.strip() == ""
    
    def test_nested_formatting(self, converter):
        """Test nested bold/italic formatting."""
        result, _, _ = converter.convert("***bold italic***")
        # Should handle reasonably (exact behavior depends on implementation)
        assert "textbf" in result or "textit" in result
    
    def test_unmatched_formatting(self, converter):
        """Test unmatched formatting markers."""
        result, _, _ = converter.convert("This has *unmatched italic")
        # Should not crash
        assert "unmatched" in result.lower()
    
    def test_very_long_table(self, converter):
        """Test table with many rows."""
        rows = ["| A | B |", "|---|---|"]
        rows.extend([f"| {i} | {i*2} |" for i in range(100)])
        markdown = "\n".join(rows)
        
        result, _, _ = converter.convert(markdown)
        assert "\\begin{tabular}" in result
    
    def test_special_unicode_in_title(self):
        """Test special characters in title."""
        compiler = LatexCompiler()
        doc = compiler._generate_document(
            title="Effect of α-blockers on β-cells",
            body="Test",
            references=[]
        )
