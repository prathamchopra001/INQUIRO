class TestLatexIntegration:
    """Integration tests for LaTeX compilation."""
    
    @pytest.fixture
    def sample_markdown(self):
        return """# Test Report

*Generated by INQUIRO — Autonomous Scientific Research System*

---

## Abstract

This is a test report generated to validate the LaTeX compilation pipeline.

## 1. Introduction

The purpose of this study is to **test** the conversion of markdown to LaTeX.

### 1.1 Background

Previous work by *Smith et al.* demonstrated that testing is important.

## 2. Methods

We used the following approach:

- Step 1: Write markdown
- Step 2: Convert to LaTeX
- Step 3: Compile to PDF

| Method | Result |
|--------|--------|
| Test 1 | Pass   |
| Test 2 | Pass   |

## 3. Results

The conversion was successful. Key findings:

1. Headers convert correctly
2. Tables render properly
3. Lists are preserved

## 4. Conclusion

Testing confirms the LaTeX pipeline works as expected.

## References

1. Smith et al. Testing Methodologies. (2024) DOI: 10.1234/test
"""
    
    def test_full_conversion_pipeline(self, sample_markdown):
        """Test full markdown to LaTeX conversion."""
        converter = MarkdownToLatexConverter()
        latex_body, refs, figs = converter.convert(sample_markdown)
        
        # Check sections converted
        assert "\\section{Abstract}" in latex_body or "Abstract" in latex_body
        assert "\\section{Introduction}" in latex_body or "Introduction" in latex_body
        
        # Check formatting converted
        assert "\\textbf{test}" in latex_body
        
        # Check table converted
        assert "\\begin{tabular}" in latex_body
    
    def test_document_generation(self, sample_markdown):
        """Test full document generation."""
        compiler = LatexCompiler()
        converter = MarkdownToLatexConverter()
        
        body, refs, _ = converter.convert(sample_markdown)
        doc = compiler._generate_document(
            title="Test Report",
            body=body,
            references=refs
        )
        
        # Check document structure
        assert "\\documentclass" in doc
        assert "\\begin{document}" in doc
        assert "\\end{document}" in doc
        assert "\\maketitle" in doc
    
    def test_compile_from_content_creates_tex(self, sample_markdown, tmp_path):
        """Test that compile_from_content creates a .tex file."""
        config = LatexConfig(compile_pdf=False)  # Don't try to compile
        compiler = LatexCompiler(config)
        
        output = compiler.compile_from_content(
            markdown_content=sample_markdown,
            title="Test Report",
            output_dir=str(tmp_path)
        )
        
        assert output is not None
        assert output.endswith(".tex")
        assert Path(output).exists()
        
        # Verify content
        content = Path(output).read_text()
        assert "\\documentclass" in content
        assert "Test Report" in content


